#Пример 1. Любые маркетплейсы(Озон, валдберис, яндексмаркет)

#Требуется высокая скорость, т.к. большое количество запросов - в пользу NoSQL.
#реляционность данных и потенциальные запросы в пользу NoSQL, хоть запросов и взаимосвязей
# очень много,но они однотипны по этому достаточно фильтрации по критериям.
# Для хранения данных достаточно котолога товаров, где у каждого товара есть свои критерии
# такие как назначение товара(мужское/женское/детское/для дома и тд), размер, цет и тд.
# и каждый товар жестко привязан к магазину/продавцу, по этому объединения в таблицу не требуется.
#Стабильность схемы данных так же в пользу NoSQL, т.к. схема нестабильна
#и требуются частые изменение схемы данных. Например, такие действия как
#добавить/удалить/изменить магазин или позиции товаров.
#Таким образом, NoSQL в данной предметной области предпочтительнее
