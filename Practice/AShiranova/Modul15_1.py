#Пример 2. МИС(Медицинская интерактивная система)
#
#Требуется высокая скорость, т.к. может быть большое количество пользователей
# и обрабатываемых запросов- в пользу NoSQL.
#реляционность данных и потенциальные запросы в пользу SQL,
# так как много различных взаимосвязей - требующих объединение данных пациента по блокам,
#строгой разбивки на структуры и большого количества отчетностей. Запросы в разных блоках
# объеденяются по группам и выводятся часто таблицами/списками
#Стабильность схемы данных в пользу NoSQL, т.к. схема нестабильна
#и требуются частые изменение схемы данных. Например, такие действия как
#добавить/удалить/изменить филтры, разделы, услуги.
# NoSQL в данной предметной области предпочтительнее, потому что все же скорость работы
#и постоянные усовершенствования продукта важнее.